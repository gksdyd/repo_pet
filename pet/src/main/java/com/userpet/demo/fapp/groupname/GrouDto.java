package com.userpet.demo.fapp.groupname;

public class GrouDto {

	private String grouSeq;
	private String grouName;
	private int deleNy;
	
	public String getGrouSeq() {
		return grouSeq;
	}
	public void setGrouSeq(String grouSeq) {
		this.grouSeq = grouSeq;
	}
	public String getGrouName() {
		return grouName;
	}
	public void setGrouName(String grouName) {
		this.grouName = grouName;
	}
	public int getDeleNy() {
		return deleNy;
	}
	public void setDeleNy(int deleNy) {
		this.deleNy = deleNy;
	}
}
